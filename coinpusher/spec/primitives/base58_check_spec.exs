defmodule Base58CheckSpec do
  use ESpec
  alias CoinPusher.Base58

  describe "base58 check encoding" do
    it do: expect Base58.encode_check(
      <<0x00, 0x65, 0xa1, 0x60, 0x59, 0x86, 0x4a, 0x2f, 0xdb, 0xc7, 0xc9, 
      0x9a, 0x47, 0x23, 0xa8, 0x39, 0x5b, 0xc6, 0xf1, 0x88, 0xeb>>
    ) |> to(eq '1AGNa15ZQXAZUgFiqJ2i7Z2DPU2J6hW62i')

    it do: expect Base58.encode_check(
      <<0x6F, 0x26, 0x1f, 0x83, 0x56, 0x8a, 0x09, 0x8a, 0x86, 0x38, 0x84, 
      0x4b, 0xd7, 0xae, 0xca, 0x03, 0x9d, 0x5f, 0x23, 0x52, 0xc0>>
    ) |> to(eq 'mizXiucXRCsEriQCHUkCqef9ph9qtPbZZ6')
  end
end
